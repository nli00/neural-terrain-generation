class Transformer_Evaluator():
    def __init__(self, config):
        self.config = config

        if torch.cuda.is_available:
            self.device = 'cuda'
        else:
            self.device = 'cpu'
            raise RuntimeWarning("Failed to detect CUDA capable GPU; defaulting to CPU")

        self.model = Transformer(config)

    def load_data(self, data: EncodedImageDataset):
        dataloader = torch.utils.data.Dataloader(
            data,
            batch_size = self.config["batch_size"],
            shuffle = False,
            num_workers = self.config["num_workers"],
            pin_memory = True
        )

        return dataloader

    def cross_entropy_loss(self, logits, labels, reduction = 'sum'):
        return F.cross_entropy(
                    logits.view(-1, logits.size(-1)), # B * L, classes view
                    labels.view(-1), # B * L, index is class
                    reduction = reduction
                )

    def top_k_count(self, logits, labels, k):
        # logits: (batch, seq_len, classes), labels: (batch, seq_len)
        with torch.no_grad():
            # Flatten to (B*L, C) and (B*L,)
            B, L, C = logits.shape
            logits_flat = logits.view(-1, C)
            labels_flat = labels.view(-1)

            # Mask out ignored positions
            valid_mask = labels_flat != -100
            valid_logits = logits_flat[valid_mask]
            valid_labels = labels_flat[valid_mask]

            if valid_logits.size(0) == 0:
                # Avoid division by zero
                return 0, 0

            # Find top-n predicted indices
            topk = torch.topk(valid_logits, k, dim=-1).indices  # Shape: (N_pred, n)

            expanded_labels = valid_labels.view(-1, 1).expand(-1, k) # Match label to logits shape
            correct = (topk == expanded_labels).any(dim=-1) # Compare if any match per row
            count = correct.sum().item() # Count matches
            total = correct.numel()
            return count, total

    def compute_batch_metrics(self, logits, labels):
        loss_sum = self.cross_entropy_loss(logits, labels, reduction = 'sum')
        top_1_count, n_preds = self.top_k_count(logits, labels, k = 1)
        top_5_count, _ = self.top_k_count(logits, labels, k = 5)

        return {
            "loss_sum": loss_sum.item(),
            "top_1_sum": top_1_count,
            "top_5_sum": top_5_count,
        }, n_preds

    def evaluate_mlm(self, data: EncodedImageDataset, checkpoint_path: str, mask_proportions: list = [.2, .4, .6, .8]):
        dataloader = self.load_data(data)
        self.model.load_checkpoint(checkpoint_path)
        
        self.model.eval()

        batch_iter = tqdm(dataloader,  leave=True)

        results = {
            p: {"loss_sum": 0, "top_1_sum": 0, "top_5_sum": 0, "total_preds": 0} 
            for p in mask_proportions
        }

        for proportion in mask_proportions:
            mask_scheduler = lambda x: torch.full_like(x, proportion)
            num_steps = None

            for batch in batch_iter:
                batch = batch.to(self.device, non_blocking=True).long() # Cast to long here because idx were saved as smaller data
                cur_batch_size = batch.size(0)

                input_ids, labels = mask_inputs(
                    batch,
                    self.mask_token_id,
                    mask_scheduler,
                    ignore_index=-100,
                )

                assert not (labels == -100).all(), f"Batch is entirely unmasked. Check masking logic. {labels[labels != -100].shape, labels[labels == -100].shape}"

                logits = self.model(input_ids)
                batch_metrics = self.compute_batch_metrics(logits, labels)

                for metric_name, val in batch_metrics.items():
                    results[proportion][metric_name] += val

            batch_iter = tqdm(dataloader,  leave=True)

        final_report = {}
        for p, counts in results.items():
            N = counts["total_preds"]
            if N == 0: continue
            
            final_report[p] = {
                "loss": counts["loss_sum"] / N,
                "top_1_acc": counts["top_1_sum"] / N,
                "top_5_acc": counts["top_5_sum"] / N,
            }

        return final_report

