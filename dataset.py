class EncodedImageDataset(Dataset):
    """
    PyTorch Dataset for codebook indices saved in the .pt file generated by transformer_preprocessing.py.

    Each __getitem__ yields a 1D tensor corresponding to the codebook indices for a single image.
    Optionally, can also return the image index or other metadata if needed.

    Args:
        pt_path (str): Path to the .pt file containing saved indices and metadata.
        transform (callable, optional): Optional transform to apply to the indices tensor.
    """

    def __init__(self, pt_path, transform=None):
        payload = torch.load(pt_path, map_location="cpu")
        self.indices = payload["indices"]  # shape: (num_images, num_patches_per_img)
        self.num_patches = payload.get("num_patches", self.indices.shape[1])
        self.codebook_size = payload.get("codebook_size", None)
        self.storage_dtype = payload.get("storage_dtype", None)
        self.dataset_root = payload.get("dataset_root", None)
        self.dataset_kind = payload.get("dataset_kind", None)
        self.transform = transform

    def __len__(self):
        return self.indices.shape[0]

    def __getitem__(self, idx):
        if torch.is_tensor(idx):
            idx = idx.item()
        indices_1d = self.indices[idx]
        if self.transform:
            indices_1d = self.transform(indices_1d)
        return indices_1d